package handshakeCalculator;

/**
 * The Signal enum represents various signals used in a handshake.
 * Each constant in this enum corresponds to a specific type of signal in the
 * handshake sequence.
 */
public enum Signal {
    /**
     * Represents a wink signal.
     */
    WINK,

    /**
     * Represents a double blink signal.
     */
    DOUBLE_BLINK,

    /**
     * Represents a 'close your eyes' signal.
     */
    CLOSE_YOUR_EYES,

    /**
     * Represents a jump signal.
     */
    JUMP
}
