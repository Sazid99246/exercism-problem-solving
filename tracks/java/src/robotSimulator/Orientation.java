package robotSimulator;

/**
 * The Orientation enum represents the four cardinal orientations of a robot.
 * It is used to define the direction in which a robot is facing.
 */
public enum Orientation {

    /**
     * Represents the north direction.
     */
    NORTH,

    /**
     * Represents the east direction.
     */
    EAST,

    /**
     * Represents the south direction.
     */
    SOUTH,

    /**
     * Represents the west direction.
     */
    WEST

}
