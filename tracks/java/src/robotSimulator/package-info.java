/**
 * Provides the classes necessary to create a robot simulator.
 *
 * This package includes the following key classes:
 * - Robot: Represents a robot, capable of moving and changing orientation based
 * on commands.
 * - Orientation: An enumeration defining the possible orientations (NORTH,
 * EAST, SOUTH, WEST) a robot can have.
 * - GridPosition: Represents a position on a two-dimensional grid.
 *
 * The robot simulator allows the creation and manipulation of robot instances
 * on a grid.
 * It enables the simulation of basic movements such as advancing and turning,
 * and can interpret a series of commands to move a robot around the grid.
 */
package robotSimulator;
